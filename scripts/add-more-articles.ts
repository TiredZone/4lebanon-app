function getRandomImage(sectionSlug: string): string {
  const sectionImages = images[sectionSlug as keyof typeof images] || images.default
  return sectionImages[Math.floor(Math.random() * sectionImages.length)]
}

function getArticleTemplate(sectionSlug: string, index: number) {
  const templates = articleTemplates[sectionSlug] || articleTemplates.local
  return templates[index % templates.length]
}

async function main() {
  console.log('🚀 Adding more articles...\n')

  // Get author
  const { data: profiles } = await supabase.from('profiles').select('id').limit(1).single()
  if (!profiles?.id) {
    console.error('No author found. Please create a user first.')
    process.exit(1)
  }
  const authorId = profiles.id
  console.log(`👤 Using author: ${authorId}\n`)

  // Get all sections
  const { data: sections } = await supabase.from('sections').select('*').order('sort_order')
  if (!sections || sections.length === 0) {
    console.error('No sections found.')
    process.exit(1)
  }

  console.log(`📁 Found ${sections.length} sections\n`)

  let totalCreated = 0

  for (const section of sections) {
    console.log(`\n📝 Creating articles for section: ${section.name_ar} (${section.slug})`)

    for (let i = 0; i < 10; i++) {
      const template = getArticleTemplate(section.slug, i)
      const slug =
        `${template.title.substring(0, 30)}-${Date.now().toString(36)}${Math.random().toString(36).substring(2, 5)}`
          .replace(/[^\u0621-\u064A\u0660-\u0669a-zA-Z0-9\s-]/g, '')
          .replace(/\s+/g, '-')

      const { error } = await supabase.from('articles').insert({
        author_id: authorId,
        slug,
        title_ar: template.title,
        excerpt_ar: template.excerpt,
        body_md: template.body,
        cover_image_path: getRandomImage(section.slug),
        section_id: section.id,
        status: 'published',
        published_at: new Date(Date.now() - Math.random() * 14 * 24 * 60 * 60 * 1000).toISOString(),
        is_breaking: Math.random() < 0.1,
        is_featured: Math.random() < 0.2,
        sources: [],
      })

      if (error) {
        console.error(`  ❌ Error: ${error.message}`)
      } else {
        totalCreated++
        console.log(`  ✅ Created: ${template.title.substring(0, 40)}...`)
      }
    }
  }

  console.log(`\n✨ Done! Created ${totalCreated} articles.`)
}

main()
